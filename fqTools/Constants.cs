using System;

namespace fqTools
{
    /// <summary>
    /// 定数定義
    /// </summary>
    public static class Const
    {
        /// <summary>
        /// π
        /// </summary>
        public const double PI = 3.141592653589793238462643;

        /// <summary>
        /// 2π
        /// </summary>
        public const double TwoPI = 6.283185307179586476925287;

        /// <summary>
        /// Radians to degrees
        /// </summary>
        public const double Rad2Deg = 57.29577951308232087679815;

        /// <summary>
        /// Degrees to radians
        /// </summary>
        public const double Deg2Rad = 1.745329251994329576923691e-2;

        /// <summary>
        /// RPM to rad/s
        /// </summary>
        public const double Rpm2Rad = TwoPI / 60.0;

        /// <summary>
        /// rad/s to RPM
        /// </summary>
        public const double Rad2Rpm = 60.0 / TwoPI;

        /// <summary>
        /// Radians to arcseconds
        /// </summary>
        public const double Rad2As = 206264.8062470963551564734;

        /// <summary>
        /// Arcseconds to radians
        /// </summary>
        public const double As2Rad = 4.848136811095359935899141e-6;


    }
}
